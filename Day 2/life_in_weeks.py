age = input("What is your current age?")
# Assuming the user inputs their current age as a number
age = int(age)
# Calculate the number of years left until 90
years_left = 90 - age
# Calculate the number of months left until 90
months_left = years_left * 12
# Calculate the number of weeks left until 90
weeks_left = months_left * 4
# Calculate the number of days left until 90
days_left = weeks_left * 7
# Print the results
print(f"You have {years_left} years, {months_left} months, {weeks_left} weeks, and {days_left} days left if you lived to 90 years.")
# Output: You have X years, Y months, Z weeks, and W days.
# where X is the number of years left, Y is the number of months left,
# Z is the number of weeks left, and W is the number of days left until the user turns 90.
